public class VariableExample {
    public static void main(String[] args) {
        String name;
        name = "Nursalim Aja";

        String address = "Jakarta";

        int age = 28;

        System.out.println(name);
        System.out.println(age);
        System.out.println(address);

        name = "Anjas Ibrahim";
        System.out.println(name);
    }
}
