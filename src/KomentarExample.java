public class KomentarExample {
    public static void main(String[] args) {
        System.out.println(sum(10, 20));
    }

    /**
     *
     * @param val1
     * @param val2
     * @return
     */
    static int sum(int val1, int val2){
        // sum val1 plus val2
        return val1 + val2;
    }
}
